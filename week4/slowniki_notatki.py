# słowniki sa kolekcja, typem mutowalnym/zmiennym
# definicja z nawiasami {}
# kolekcja par klucz:wartość
# dictonary[key] --> value
# słowniki nie sa posortowane, nie ma tam żadnej ustalonej kolejności w słowniku
# gdy chcemy dorzucic nowa wartosc dictionary[key]=value - juz :)
# klucze musza byc unikalne, i niezmienne
# gdyby klucze byly zmienne to mogo by to sprawic ze utracilibysmy w sposob niekontorlowany nasze wartosci
# mozna użyc tupli jako klucza - jednak naleźy wowczas odwołac sie do calosci d= {(1,2,3):3} ->> d[(1,2,3)]
# gdy mamy w miejscu wartosci liste  d = {"x":[1,2,3]} ---> d["x"][1] - dopiero teraz mamy dostep do elementu 2 :P


